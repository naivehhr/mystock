// 验证股票代码和名称是否匹配
export function validateStockCode(code: string, name: string): { valid: boolean; message: string } {
  const trimmedCode = code.trim()
  const trimmedName = name.trim()
  
  if (!trimmedCode || !trimmedName) {
    return { valid: false, message: '' }
  }
  
  const correctName = STOCK_CODE_MAP[trimmedCode]
  
  if (!correctName) {
    // 代码不在映射表中，给出提示
    return { valid: true, message: '' }
  }
  
  // 简化匹配：去除"*"、"A"后比较
  const simpleName1 = trimmedName.replace(/[*\sA-Ba-b]/g, '')
  const simpleName2 = correctName.replace(/[*\sA-Ba-b]/g, '')
  
  if (simpleName1 !== simpleName2) {
    return { valid: false, message: `股票代码 ${trimmedCode} 对应的正确名称应为 "${correctName}"` }
  }
  
  return { valid: true, message: '' }
}

// 获取股票代码对应的正确名称
export function getStockName(code: string): string | undefined {
  return STOCK_CODE_MAP[code.trim()]
}
